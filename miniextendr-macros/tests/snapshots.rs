/// Normalize R wrapper output for snapshot comparison:
/// - Strip "# Generated from Rust source file: ..." header (contains env-specific paths)
/// - Strip trailing whitespace from each line (avoids git hook / editor conflicts)
fn strip_generated_header(s: &str) -> String {
    s.lines()
        .filter(|line| !line.starts_with("# Generated from Rust source file:"))
        .map(|line| line.trim_end())
        .collect::<Vec<_>>()
        .join("\n")
}

