#[test]
fn snapshot_add() {
    let wrapper = strip_generated_header(R_WRAPPER_ADD);
    expect![[r#"
        #' @source Generated by miniextendr from Rust fn `add`
          #' @export
          add <- function(x, y) {
            .Call(C_add, .call = match.call(), x, y)
          }"#]]
    .assert_eq(wrapper.trim());
}

