// -- Class system snapshots --------------------------------------------------

#[test]
fn snapshot_env_class() {
    let wrapper = strip_generated_header(R_WRAPPERS_IMPL_SNAPCOUNTER);
    expect![[r##"
        #' @title SnapCounter  Class
          #' @name SnapCounter
          #' @rdname SnapCounter
          #' @source Generated by miniextendr from Rust type `SnapCounter`
          #' @export
          SnapCounter <- new.env(parent = emptyenv())

          #' @name SnapCounter$new
          #' @rdname SnapCounter
          #' @source Generated by miniextendr from `SnapCounter::new`
          SnapCounter$new <- function() {
            self <- .Call(C_SnapCounter__new, .call = match.call())
            class(self) <- "SnapCounter"
            self
          }

          #' @description Get the current value.
          #' @name SnapCounter$get_value
          #' @rdname SnapCounter
          #' @source Generated by miniextendr from `SnapCounter::get_value`
          SnapCounter$get_value <- function() {
            .Call(C_SnapCounter__get_value, .call = match.call(), self)
          }

          #' @title Increment by a step
          #' @param step Amount to increment.
          #' @name SnapCounter$increment
          #' @rdname SnapCounter
          #' @source Generated by miniextendr from `SnapCounter::increment`
          SnapCounter$increment <- function(step) {
            .Call(C_SnapCounter__increment, .call = match.call(), self, step)
            self
          }

          #' @rdname SnapCounter
          #' @param self The object instance.
          #' @param name Method name for dispatch.
          #' @export
          `$.SnapCounter` <- function(self, name) {
            obj <- SnapCounter[[name]]
            if (is.environment(obj)) {
              # Trait namespace - bind self to all methods
              bound <- new.env(parent = emptyenv())
              for (method_name in names(obj)) {
                method <- obj[[method_name]]
                if (is.function(method)) {
                  environment(method) <- environment()
                  bound[[method_name]] <- method
                }
              }
              bound
            } else {
              environment(obj) <- environment()
              obj
            }
          }
          #' @rdname SnapCounter
          #' @export
          `[[.SnapCounter` <- `$.SnapCounter`"##]]
    .assert_eq(wrapper.trim());
}

#[test]
fn snapshot_r6_class() {
    let wrapper = strip_generated_header(R_WRAPPERS_IMPL_SNAPR6COUNTER);
    expect![[r##"
        #' @title SnapR6Counter R6 Class
          #' @name SnapR6Counter
          #' @rdname SnapR6Counter
          #' @source Generated by miniextendr from Rust type `SnapR6Counter`
          #' @importFrom R6 R6Class
          #' @param .ptr Internal pointer (used by static methods, not for direct use).
          #' @export
          SnapR6Counter <- R6::R6Class("SnapR6Counter",
            public = list(
              initialize = function(initial, .ptr = NULL) {
                if (!is.null(.ptr)) {
                  private$.ptr <- .ptr
                } else {
                  private$.ptr <- .Call(C_SnapR6Counter__new, .call = match.call(), initial)
                }
              },
              get = function() {
                .Call(C_SnapR6Counter__get, .call = match.call(), private$.ptr)
              }
            ),
            private = list(
              .ptr = NULL
            ),
            lock_objects = TRUE,
            lock_class = FALSE,
            cloneable = FALSE
          )"##]]
    .assert_eq(wrapper.trim());
}
