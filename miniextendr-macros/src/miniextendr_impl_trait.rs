/// Generate R function body preamble lines (r_entry, on.exit, lifecycle, r_post_checks).
///
/// Returns lines to insert at the top of the R function body, before the `.Call()`.
fn trait_method_preamble_lines(method: &TraitMethod, indent: &str) -> Vec<String> {
    let mut lines = Vec::new();

    // r_entry: inject at the very top
    if let Some(ref entry) = method.r_entry {
        for line in entry.lines() {
            lines.push(format!("{}{}", indent, line));
        }
    }

    // on.exit: register cleanup
    if let Some(ref on_exit) = method.r_on_exit {
        lines.push(format!("{}{}", indent, on_exit.to_r_code()));
    }

    // lifecycle: deprecation/experimental warnings
    if let Some(ref spec) = method.lifecycle {
        let what = method.r_method_name();
        if let Some(prelude) = spec.r_prelude(&what) {
            for line in prelude.lines() {
                lines.push(format!("{}{}", indent, line));
            }
        }
    }

    // r_post_checks: inject after all checks, before .Call()
    if let Some(ref post) = method.r_post_checks {
        for line in post.lines() {
            lines.push(format!("{}{}", indent, line));
        }
    }

    lines
}

/// Generate R function body lines with optional error_in_r checking.
///
/// Without error_in_r: returns `["{indent}{call_expr}"]`
/// With error_in_r: captures result in `.val`, checks for `rust_condition_value`, returns `.val`
fn trait_method_body_lines(call_expr: &str, error_in_r: bool, indent: &str) -> Vec<String> {
    if error_in_r {
        let mut lines = vec![format!("{}.val <- {}", indent, call_expr)];
        lines.extend(crate::method_return_builder::error_in_r_check_lines(indent));
        lines.push(format!("{}.val", indent));
        lines
    } else {
        vec![format!("{}{}", indent, call_expr)]
    }
}

/// Convert a type to an uppercase identifier-safe name.
///
/// Examples:
/// - `MyType` → `MYTYPE`
/// - `path::to::MyType` → `MYTYPE` (uses last segment)
/// - `MyType<T>` → `MYTYPE` (strips generics)
fn type_to_uppercase_name(ty: &syn::Type) -> String {
    match ty {
        syn::Type::Path(type_path) => {
            // Use last segment, strip generics
            type_path
                .path
                .segments
                .last()
                .map(|s| s.ident.to_string().to_uppercase())
                .unwrap_or_else(|| "UNKNOWN".to_string())
        }
        _ => "UNKNOWN".to_string(),
    }
}

fn path_has_generic_args(path: &syn::Path) -> bool {
    path.segments
        .iter()
        .any(|segment| !matches!(segment.arguments, syn::PathArguments::None))
}

fn type_has_generic_args(ty: &syn::Type) -> bool {
    match ty {
        syn::Type::Path(type_path) => path_has_generic_args(&type_path.path),
        _ => false,
    }
}

fn stable_symbol_hash(input: &str) -> String {
    // FNV-1a: simple, deterministic, and independent of std hasher seeding.
    let mut hash = 0xcbf2_9ce4_8422_2325u64;
    for byte in input.as_bytes() {
        hash ^= u64::from(*byte);
        hash = hash.wrapping_mul(0x0000_0100_0000_01b3);
    }
    format!("{hash:016X}")
}

fn vtable_type_symbol_name(trait_path: &syn::Path, concrete_type: &syn::Type) -> String {
    let mut type_name = type_to_uppercase_name(concrete_type);
    if path_has_generic_args(trait_path) || type_has_generic_args(concrete_type) {
        let discriminator = quote::quote!(#trait_path for #concrete_type).to_string();
        type_name.push_str("_H");
        type_name.push_str(&stable_symbol_hash(&discriminator));
    }
    type_name
}
