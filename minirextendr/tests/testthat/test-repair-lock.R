# Tests for miniextendr_repair_lock()

# Minimal project fixture: DESCRIPTION + NAMESPACE + src/rust/Cargo.toml
make_lock_project <- function(lock_content = NULL) {
  tmp <- withr::local_tempdir(pattern = "repair-lock-")
  usethis::proj_set(tmp, force = TRUE)

  writeLines(
    "Package: testpkg\nTitle: Test\nVersion: 0.1.0\n",
    file.path(tmp, "DESCRIPTION")
  )
  writeLines("", file.path(tmp, "NAMESPACE"))

  rust_dir <- file.path(tmp, "src", "rust")
  dir.create(rust_dir, recursive = TRUE, showWarnings = FALSE)

  writeLines(c(
    '[package]',
    'name = "testpkg"',
    'version = "0.1.0"',
    'edition = "2021"',
    '',
    '[lib]',
    'crate-type = ["staticlib"]',
    '',
    '[dependencies]'
  ), file.path(rust_dir, "Cargo.toml"))

  if (!is.null(lock_content)) {
    writeLines(lock_content, file.path(rust_dir, "Cargo.lock"))
  }

  tmp
}

# Canonical tarball-shape lock (no checksums, no path+ sources)
tarball_shape_lock <- c(
  '# This file is automatically @generated by Cargo.',
  '# It is not intended for manual editing.',
  'version = 3',
  '',
  '[[package]]',
  'name = "testpkg"',
  'version = "0.1.0"',
  '',
  '[[package]]',
  'name = "miniextendr-api"',
  'version = "0.1.0"',
  'source = "git+https://github.com/A2-ai/miniextendr#abc123"'
)

# Source-shape: has checksum lines
source_shape_with_checksums <- c(
  '# This file is automatically @generated by Cargo.',
  'version = 3',
  '',
  '[[package]]',
  'name = "testpkg"',
  'version = "0.1.0"',
  '',
  '[[package]]',
  'name = "libc"',
  'version = "0.2.150"',
  'source = "registry+https://github.com/rust-lang/crates.io-index"',
  'checksum = "89d92a4743f9a61002fae18374ed11e7973f530cb3b3e0b4b63760b6d924afb5"'
)

# Source-shape: has path+ entries (from [patch."git+url"])
source_shape_with_path <- c(
  '# This file is automatically @generated by Cargo.',
  'version = 3',
  '',
  '[[package]]',
  'name = "testpkg"',
  'version = "0.1.0"',
  '',
  '[[package]]',
  'name = "miniextendr-api"',
  'version = "0.1.0"',
  'source = "path+file:///home/user/miniextendr/miniextendr-api"'
)

# ── Tests ──────────────────────────────────────────────────────────────────

test_that("miniextendr_repair_lock returns FALSE invisibly when lock is already in tarball-shape", {
  tmp <- make_lock_project(tarball_shape_lock)
  on.exit(usethis::proj_set(getwd(), force = TRUE, quiet = TRUE), add = TRUE)

  result <- miniextendr_repair_lock(tmp, quiet = TRUE)

  expect_false(result)
  # No changes: lock content is identical
  lock_path <- file.path(tmp, "src", "rust", "Cargo.lock")
  expect_equal(readLines(lock_path, warn = FALSE), tarball_shape_lock)
})

test_that("miniextendr_repair_lock strips checksum lines and returns TRUE", {
  # This test does NOT call cargo — it exercises only the checksum-stripping
  # logic by starting from a lock that already has git+ sources (no path+).
  tmp <- make_lock_project(source_shape_with_checksums)
  on.exit(usethis::proj_set(getwd(), force = TRUE, quiet = TRUE), add = TRUE)

  lock_path <- file.path(tmp, "src", "rust", "Cargo.lock")

  # Monkeypatch run_with_logging to a no-op that leaves the lock unchanged
  # (simulates cargo update completing without touching the file).
  # We inject a local binding in the package environment for this call.
  local_mocked_bindings(
    run_with_logging = function(command, args, log_prefix, wd) {
      list(status = NULL, output = character(), log_file = "", success = TRUE)
    },
    .package = "minirextendr",
    {
      result <- miniextendr_repair_lock(tmp, quiet = TRUE)
    }
  )

  expect_true(result)

  remaining <- readLines(lock_path, warn = FALSE)
  expect_false(any(grepl("^checksum = ", remaining)))
  # Original non-checksum lines are preserved
  expect_true(any(grepl('^name = "libc"', remaining)))
})

test_that("miniextendr_repair_lock errors with clear message when lockfile is missing", {
  tmp <- make_lock_project(lock_content = NULL)  # no Cargo.lock written
  on.exit(usethis::proj_set(getwd(), force = TRUE, quiet = TRUE), add = TRUE)

  expect_error(
    miniextendr_repair_lock(tmp),
    regexp = "Cargo\\.lock"
  )
})

test_that("miniextendr_repair_lock moves .cargo/config.toml aside and restores it", {
  tmp <- make_lock_project(source_shape_with_checksums)
  on.exit(usethis::proj_set(getwd(), force = TRUE, quiet = TRUE), add = TRUE)

  cargo_dir <- file.path(tmp, "src", "rust", ".cargo")
  dir.create(cargo_dir, recursive = TRUE, showWarnings = FALSE)
  cfg_path <- file.path(cargo_dir, "config.toml")
  writeLines(
    c('[patch."git+https://github.com/A2-ai/miniextendr"]',
      'miniextendr-api = { path = "../../../../miniextendr-api" }'),
    cfg_path
  )

  local_mocked_bindings(
    run_with_logging = function(command, args, log_prefix, wd) {
      # Verify config.toml was moved aside during the call
      expect_false(
        file.exists(cfg_path),
        label = ".cargo/config.toml must be absent during cargo update"
      )
      bak <- paste0(cfg_path, ".tmp_repair_lock")
      expect_true(
        file.exists(bak),
        label = ".cargo/config.toml backup must exist during cargo update"
      )
      list(status = NULL, output = character(), log_file = "", success = TRUE)
    },
    .package = "minirextendr",
    {
      miniextendr_repair_lock(tmp, quiet = TRUE)
    }
  )

  # Config is restored after the call
  expect_true(file.exists(cfg_path))
  expect_false(file.exists(paste0(cfg_path, ".tmp_repair_lock")))
})

test_that("miniextendr_repair_lock errors when path+ entries remain after cargo update", {
  # Simulate a lock where cargo update didn't remove path+ entries (e.g.,
  # the Cargo.toml itself declares a path dep, not via [patch]).
  tmp <- make_lock_project(source_shape_with_path)
  on.exit(usethis::proj_set(getwd(), force = TRUE, quiet = TRUE), add = TRUE)

  local_mocked_bindings(
    run_with_logging = function(command, args, log_prefix, wd) {
      # Leave the lock unchanged — path+ entries still present
      list(status = NULL, output = character(), log_file = "", success = TRUE)
    },
    .package = "minirextendr",
    {
      expect_error(
        miniextendr_repair_lock(tmp, quiet = TRUE),
        regexp = "path\\+"
      )
    }
  )
})
